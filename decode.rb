def decode morse
  morse_ascii = [
    "-----",
    ".----",
    "..---",
    "...--",
    "....-",
    ".....",
    "-....",
    "--...",
    "---..",
    "----.",
    "---...",
    "-.-.-.",
    "<",
    "-...-",
    ">",
    "..--..",
    ".--.-.",
    ".-",
    "-...",
    "-.-.",
    "-..",
    ".",
    "..-.",
    "--.",
    "....",
    "..",
    ".---",
    "-.-",
    ".-..",
    "--",
    "-.",
    "---",
    ".--.",
    "--.-",
    ".-.",
    "...",
    "-",
    "..-",
    "...-",
    ".--",
    "-..-",
    "-.--",
    "--.."
   ] 
 decoded = ''
 morse_words = morse.split('  ')
 morse_words.each {|w| 
  w.split(' ').each {|c| decoded += (morse_ascii.index(c)+48).chr}
  decoded += ' '
}
decoded
end

puts decode "-- -.--   -. .- -- ."
